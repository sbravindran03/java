package control_statements.java_if_else_;

public class nested_easyeg {
    public static void main(String[] args) {
        for (int j = 1; j <= 5; j++) {
            for (int i = 1; i <= j; i++) {
                System.out.print(i);
            }
            System.out.println("\n");

        }
    }

}
